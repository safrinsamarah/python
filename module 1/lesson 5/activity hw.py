temperature = float(input("Enter the current temperature (10 - 100°C): "))
if temperature == 10:
    print("Wear a heavy jacket, sweater, scarf, gloves, and boots.")
elif temperature == 20:
    print("Wear a light jacket or hoodie with jeans.")
elif temperature == 30:
    print("Wear a T-shirt with jeans or two piece.")
elif temperature == 40:
    print("Wear light cotton clothes such as a T-shirt and shorts or light or air pass dress.")
else:
    print("INVALID INPUT")